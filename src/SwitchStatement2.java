public class SwitchStatement2 {
    public static void main(String args[]){
        int x = 3;
        switch (x){
            case 0:
                System.out.println(0);
            case 1:
                System.out.println(1);
                break;
            case 2:
                System.out.println(2);
                break;
            default:
                System.out.println("default statement");
        }

    }
}
